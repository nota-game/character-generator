	function transformArmor(a: RüstungDefinition_kampf_ausstattung): {
		titel: string;
		steifheit: number;
		härte: number;
		dämpfung: number;
		range: (readonly [number, number])[];
	} {
		return {
			härte: a?.Schutz.Härte?.Wert ?? 0,
			dämpfung: a?.Schutz.Dämpfung?.Wert ?? 0,
			steifheit: a?.Schutz.Flexibilität?.Wert ?? 0,
			titel: getText(a?.Name),
			range: keys(a?.Trefferzonen).flatMap((g) => {
				const regionConst =
					g == 'Kopf'
						? 0
						: g == 'LinkerArm'
						? 10
						: g == 'RechterArm'
						? 20
						: g == 'Brust'
						? 30
						: g == 'Hüfte'
						? 40
						: g == 'RechtesBein'
						? 50
						: g == 'LinkesBein'
						? 60
						: -1000;
				return (
					a?.Trefferzonen[g]?.Schutz.map(
						(x) => [x.Von! + regionConst, x.Bis! + regionConst] as const
					) ?? []
				);
			})
		};
	}
